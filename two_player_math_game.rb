# Generates a random number between 1 and 20.
def random_num
  rand(1..20)
end

# Takes in and adds two numbers, returns sum.
def add(a, b)
  a + b
end

# Takes in and subtracts two numbers, returns difference.
def subtract(a, b)
  a - b
end

# Takes in and multiplies two numbers, returns product.
def multiply(a, b)
  a * b
end

puts add(3, 4)
puts subtract(5, 3)
puts multiply(3, 2)